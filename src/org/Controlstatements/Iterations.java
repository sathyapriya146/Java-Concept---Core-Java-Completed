package org.Controlstatements;

public class Iterations {
public static void main(String[] args) {
	int i = 3;
	System.out.println(i++);
	System.out.println(++i);
	System.out.println(i++);
	System.out.println(i++);
	System.out.println(++i);
	System.out.println(i++);
	System.out.println(i++ + ++i);
	System.out.println(i++);
	
	
}
}
